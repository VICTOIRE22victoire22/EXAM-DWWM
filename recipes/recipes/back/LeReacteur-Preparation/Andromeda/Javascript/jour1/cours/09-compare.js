// Les comparateurs possibles :

// === : égal à

// !== : différent de

// > : strictement supérieur à

// >=  : supérieur ou égal à

// < : strictement inférieur à

// <= : inférieur ou égal à

const num1 = 4;
const num2 = "4";

if (num1 === num2) {
  console.log("Ils sont identiques");
} else {
  console.log("Ils sont différents");
}

